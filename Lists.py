# Create a list using []
a = [1, 2, 4, 56, 6]

# Print the list using print() function
print(a) # Output is same as given in the list.

# Access index using a[0], a[1], a[2]
print(a[2])

# Change the value of list using
a[0] = 98
print(a)

# We can create a list with items of different types
c = [45, "Harry", False, 6.9]
print(c) # In a list we can store values of any data type, but not in array.

# List slicing
friends = ["Harry", "Tom", "Roshan", "Sam", "Divya", 45]
print(friends[0:4]) # Output will in form of a list but same numbering as string.
print(friends[-4:]) # Output ['Roshan', 'Sam', 'Divya', 45]. Lists are in single quoted format.
